export type TupperTranslationKey = keyof typeof translations.es;

export function useTupperTranslations(locale: Locale) {
  const current = translations[locale] ?? translations[defaultLocale];

  return function tupperT(key: TupperTranslationKey) {
    return current[key] ?? translations[defaultLocale][key] ?? key;
  };
}
